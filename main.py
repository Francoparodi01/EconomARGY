# Variable para guardar la √∫ltima fecha procesada
last_notified_time = None

async def check_dolar_changes(context: CallbackContext):
    global last_notified_time  # Usamos la variable global para rastrear la √∫ltima fecha procesada

    try:
        # Obtener valores actuales desde la API
        current_dolar_values = await get_dolar_values()

        # Si no hay valores previos registrados en la variable global, obtener el √∫ltimo registro de la BD
        if not last_notified_time:
            last_entry = collection.find_one(sort=[("ultimaActualizacion", -1)])  # Obtener el registro m√°s reciente
            if last_entry:
                last_notified_time = datetime.strptime(last_entry["ultimaActualizacion"], "%Y-%m-%dT%H:%M:%S.%fZ")

        # Procesar nuevos datos
        new_data = []
        for dolar in current_dolar_values:
            # Convertir fecha de actualizaci√≥n
            dolar_datetime = datetime.strptime(dolar["fechaActualizacion"], "%Y-%m-%dT%H:%M:%S.%fZ")

            # Agregar a la lista de nuevos datos solo si es m√°s reciente que la √∫ltima notificaci√≥n
            if not last_notified_time or dolar_datetime > last_notified_time:
                new_data.append(dolar)

        if new_data:
            # Generar mensaje de notificaci√≥n
            message = "üíµ *Actualizaci√≥n de los valores del d√≥lar:*\n\n"
            for dolar in new_data:
                message += (
                    f"üè† Casa: {dolar['nombre']} ({dolar['casa']})\n"
                    f"üü¢ Compra: {dolar['compra']} ARS\n"
                    f"üî¥ Venta: {dolar['venta']} ARS\n"
                    f"üìÖ Fecha: {dolar['fechaActualizacion']}\n\n"
                )

            # Guardar los datos nuevos en la base de datos
            save_dolar_to_db(new_data)

            # Actualizar la √∫ltima fecha procesada
            last_notified_time = max(
                datetime.strptime(dolar["fechaActualizacion"], "%Y-%m-%dT%H:%M:%S.%fZ")
                for dolar in new_data
            )

            # Enviar notificaci√≥n al usuario
            await context.bot.send_message(chat_id=chat_id_user, text=message, parse_mode="Markdown")
        else:
            print("No hay nuevos datos.")
    except Exception as e:
        print(f"‚ö†Ô∏è Error en la detecci√≥n de cambios: {e}")


# Funci√≥n para obtener la √∫ltima hora de actualizaci√≥n guardada en la base de datos
def get_last_update_time():
    """Obtener la √∫ltima hora de actualizaci√≥n guardada en la base de datos."""
    last_entry = collection.find_one(sort=[("ultimaActualizacion", -1)])  # Ordenar por el campo m√°s reciente
    if last_entry:
        return datetime.strptime(last_entry["ultimaActualizacion"], "%Y-%m-%dT%H:%M:%S.%fZ")
    return None
        
# Comando para iniciar el monitoreo
async def start_check_dolar(update: Update, context: CallbackContext):
    try: 
        context.job_queue.run_repeating(check_dolar_changes, interval=60, first=5)
        await update.message.reply_text("‚è≥ Monitoreo del d√≥lar iniciado. Te notificar√© si hay cambios.")
    except Exception as e:
        print(f"Error al iniciar el monitoreo del d√≥lar: {e}")
        await update.message.reply_text("‚ö†Ô∏è Ocurri√≥ un error al intentar iniciar el monitoreo del d√≥lar.")

def main():
    print("Iniciando el bot de Telegram...")

    # Iniciar el bot de Telegram
    app = Application.builder().token(token).build()

    # Manejar comandos
    app.add_handler(CommandHandler("start", start))
    app.add_handler(CommandHandler("help", help))
    app.add_handler(CommandHandler("dolar", get_dolar))
    app.add_handler(CommandHandler("check_dolar", start_check_dolar))

    # Manejar mensajes de texto
    app.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, handle_message))

    # Iniciar el bot
    app.run_polling()

if __name__ == "__main__":
    main()